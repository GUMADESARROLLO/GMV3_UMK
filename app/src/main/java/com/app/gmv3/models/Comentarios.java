package com.app.gmv3.models;

public class Comentarios {
    String Titulo;
    String Contenido;
    String Fecha;
    String Autor;
    String Imagen;

    public String getTitulo() {
        return Titulo;
    }

    public String getContenido() {
        return Contenido;
    }

    public String getFecha() {
        return Fecha;
    }

    public String getAutor() {
        return Autor;
    }

    public String getImagen() {
        return Imagen;
    }
}
