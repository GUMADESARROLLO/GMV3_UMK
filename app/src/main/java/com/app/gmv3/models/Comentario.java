package com.app.gmv3.models;

public class Comentario {
    String id_coment;
    String orden_code;
    String orden_comment;
    String date_coment;
    String player_id;

    public String getId_coment() {
        return id_coment;
    }

    public String getOrden_code() {
        return orden_code;
    }

    public String getOrden_comment() {
        return orden_comment;
    }

    public String getDate_coment() {
        return date_coment;
    }

    public String getPlayer_id() {
        return player_id;
    }
}
